/* Excercise: 1.1.1
 * Program: helloWorld10Times
 * Description: Prints the string "Hello World" 10 times.
 */
public class helloWorld10Times {
    public static void main(String[] args) {
        for (int i = 0; i < 10; i++) {
            System.out.println(i + ": Hello World");
        }
    }
}
